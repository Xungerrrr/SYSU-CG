void framebuffer_size_callback(GLFWwindow* window, int width, int height);
void processInput(GLFWwindow* window);
void mouse_callback(GLFWwindow* window, double xpos, double ypos);
void scroll_callback(GLFWwindow* window, double xoffset, double yoffset);
void initPlane(unsigned int&, unsigned int&);
void initCube(unsigned int&, unsigned int&);
void renderScene(Shader& shader, unsigned int&, unsigned int&);
void initQuad(unsigned int&, unsigned int&);
void initDepthMap(unsigned int&, unsigned int&, const unsigned int, const unsigned int);
unsigned int loadTexture(const char* path);

int windowWidth = 1280;
int windowHeight = 720;

float deltaTime = 0.0f; // Time diffence between two frames
float lastFrame = 0.0f; // The time of last frame
bool firstMouse = true;
float lastX = windowWidth / 2, lastY = windowHeight / 2;

Camera camera = Camera(glm::vec3(10.0f, 15.0f, -20.0f));

int main() {
	// Init GLFW
	glfwInit();
	glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
	glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 3);
	glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);

	GLFWwindow* window = glfwCreateWindow(windowWidth, windowHeight, "CG_HW7", nullptr, nullptr);
	if (window == NULL) {
		std::cout << "Failed to create GLFW window" << std::endl;
		glfwTerminate();
		return -1;
	}
	glfwMakeContextCurrent(window);
	if (!gladLoadGLLoader((GLADloadproc)glfwGetProcAddress)) {
		std::cout << "Failed to initialize GLAD" << std::endl;
		return -1;
	}
	glfwSetFramebufferSizeCallback(window, framebuffer_size_callback);
	// glfwSetCursorPosCallback(window, mouse_callback);
	glfwSetScrollCallback(window, scroll_callback);
	// glfwSetInputMode(window, GLFW_CURSOR, GLFW_CURSOR_DISABLED);

	glEnable(GL_DEPTH_TEST);

	// 创建着色器程序
	Shader depthShader("depth.vs", "depth.fs");
	Shader quadShader("quad.vs", "quad.fs");
	Shader shader("shadow.vs", "shadow.fs");
	Shader lightShader("light.vs", "light.fs");

	unsigned int planeVAO, planeVBO;
	initPlane(planeVAO, planeVBO);

	unsigned int quadVAO, quadVBO;
	initQuad(quadVAO, quadVBO);

	unsigned int cubeVAO, cubeVBO;
	initCube(cubeVAO, cubeVAO);

	unsigned int depthMapFBO, depthMap;
	const unsigned int SHADOW_WIDTH = 4096, SHADOW_HEIGHT = 4096;
	initDepthMap(depthMapFBO, depthMap, SHADOW_WIDTH, SHADOW_HEIGHT);

	unsigned int woodTexture = loadTexture("wood.jpg");

	// lighting
	glm::vec3 lightPos(-3.0f, 4.0f, -1.0f);

	shader.use();
	shader.setInt("diffuseTexture", 0);
	shader.setInt("shadowMap", 1);
	quadShader.use();
	quadShader.setInt("depthMap", 0);

	// 初始化ImGui
	ImGui::CreateContext();
	ImGuiIO &io = ImGui::GetIO(); (void)io;
	ImGui::StyleColorsDark();
	ImGui_ImplGlfw_InitForOpenGL(window, true);
	ImGui_ImplOpenGL3_Init("#version 330");

	int projectionType = 1;

	while (!glfwWindowShouldClose(window)) {
		// 处理输入
		processInput(window);

		// 黑色清屏
		glClearColor(0.1f, 0.1f, 0.1f, 1.0f);
		glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
		
		float time = (float)glfwGetTime();
		deltaTime = time - lastFrame;
		lastFrame = time;

		ImGui_ImplOpenGL3_NewFrame();
		ImGui_ImplGlfw_NewFrame();
		ImGui::NewFrame();

		ImGui::Begin("Projection Type");
		ImGui::BeginGroup();
		ImGui::RadioButton("Orthogonal", &projectionType, 1);
		ImGui::RadioButton("Perspective", &projectionType, 2);
		ImGui::EndGroup();
		ImGui::End();


		glm::mat4 lightView = glm::mat4(1.0f);
		glm::mat4 lightProjection = glm::mat4(1.0f);
		glm::mat4 lightSpaceMatrix = glm::mat4(1.0f);

		lightPos.z = sin(time) * 10.0f;
		lightPos.y = sin(time/ 2.0f) * 2 + 4.0f;

		float near_plane = 1.0f, far_plane = 100.0f;
		if (projectionType == 1)
			lightProjection = glm::ortho(-10.0f, 10.0f, -10.0f, 10.0f, near_plane, far_plane);
		else if (projectionType == 2)
			lightProjection = glm::perspective(glm::radians(120.0f), 1.0f, near_plane, far_plane);
		lightView = glm::lookAt(lightPos, glm::vec3(0.0f), glm::vec3(0.0, 1.0, 0.0));
		lightSpaceMatrix = lightProjection * lightView;

		depthShader.use();
		depthShader.setMat4("lightSpaceMatrix", lightSpaceMatrix);

		glViewport(0, 0, SHADOW_WIDTH, SHADOW_HEIGHT);
		glBindFramebuffer(GL_FRAMEBUFFER, depthMapFBO);
		glClear(GL_DEPTH_BUFFER_BIT);
		// glCullFace(GL_FRONT);
		renderScene(depthShader, planeVAO, cubeVAO);
		// glCullFace(GL_BACK);
		glBindFramebuffer(GL_FRAMEBUFFER, 0);

		glViewport(0, 0, windowWidth, windowHeight);
		glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
		shader.use();
		glm::mat4 projection = glm::perspective(camera.getFov(), (float)windowWidth / (float)windowHeight, 0.1f, 100.0f);
		glm::mat4 view = camera.getViewMatrix();
		shader.setMat4("projection", projection);
		shader.setMat4("view", view);
		// set light uniforms
		shader.setVec3("viewPos", camera.getPosition());
		shader.setVec3("lightPos", lightPos);
		shader.setMat4("lightSpaceMatrix", lightSpaceMatrix);
		glActiveTexture(GL_TEXTURE0);
		glBindTexture(GL_TEXTURE_2D, woodTexture);
		glActiveTexture(GL_TEXTURE1);
		glBindTexture(GL_TEXTURE_2D, depthMap);
		renderScene(shader, planeVAO, cubeVAO);

		quadShader.use();
		quadShader.setFloat("near_plane", near_plane);
		quadShader.setFloat("far_plane", far_plane);
		glActiveTexture(GL_TEXTURE0);
		glBindTexture(GL_TEXTURE_2D, depthMap);
		//glBindVertexArray(quadVAO);
		//glDrawArrays(GL_TRIANGLE_STRIP, 0, 4);
		//glBindVertexArray(0);
		
		lightShader.use();
		lightShader.setMat4("projection", projection);
		lightShader.setMat4("view", view);
		glm::mat4 model = glm::mat4(1.0f);
		model = glm::translate(model, lightPos);
		model = glm::scale(model, glm::vec3(0.2f)); // a smaller cube
		lightShader.setMat4("model", model);
		// render Cube
		glBindVertexArray(cubeVAO);
		glDrawArrays(GL_TRIANGLES, 0, 36);
		glBindVertexArray(0);

		// 渲染ImGui
		ImGui::Render();
		ImGui_ImplOpenGL3_RenderDrawData(ImGui::GetDrawData());

		glfwPollEvents();
		glfwSwapBuffers(window);
	}
	ImGui_ImplOpenGL3_Shutdown();
	ImGui_ImplGlfw_Shutdown();
	ImGui::DestroyContext();
	glfwDestroyWindow(window);
	glfwTerminate();
	return 0;
}

void processInput(GLFWwindow* window) {
	if (glfwGetKey(window, GLFW_KEY_ESCAPE) == GLFW_PRESS)
		glfwSetWindowShouldClose(window, true);
	float cameraSpeed = 10.0f * deltaTime;
	if (glfwGetKey(window, GLFW_KEY_W) == GLFW_PRESS)
		camera.moveForward(cameraSpeed);
	if (glfwGetKey(window, GLFW_KEY_S) == GLFW_PRESS)
		camera.moveBack(cameraSpeed);
	if (glfwGetKey(window, GLFW_KEY_A) == GLFW_PRESS)
		camera.moveLeft(cameraSpeed);
	if (glfwGetKey(window, GLFW_KEY_D) == GLFW_PRESS)
		camera.moveRight(cameraSpeed);
	if (glfwGetKey(window, GLFW_KEY_M) == GLFW_PRESS)
		glfwSetInputMode(window, GLFW_CURSOR, GLFW_CURSOR_NORMAL);
	if (glfwGetKey(window, GLFW_KEY_N) == GLFW_PRESS)
		glfwSetInputMode(window, GLFW_CURSOR, GLFW_CURSOR_DISABLED);
}

void mouse_callback(GLFWwindow * window, double xpos, double ypos) {
	if (firstMouse) {
		lastX = xpos;
		lastY = ypos;
		firstMouse = false;
	}

	float xoffset = xpos - lastX;
	float yoffset = lastY - ypos;
	lastX = xpos;
	lastY = ypos;

	float sensitivity = 0.1;
	xoffset *= sensitivity;
	yoffset *= sensitivity;
	camera.rotate(yoffset, xoffset);
}

void scroll_callback(GLFWwindow * window, double xoffset, double yoffset)
{
	camera.zoom(yoffset);
}

void renderScene(Shader& shader, unsigned int &planeVAO, unsigned int &cubeVAO) {
	// Floor
	glm::mat4 model = glm::mat4(1.0f);
	shader.setMat4("model", model);
	glBindVertexArray(planeVAO);
	glDrawArrays(GL_TRIANGLES, 0, 6);
	glBindVertexArray(0);
	// Cubes
	model = glm::mat4(1.0f);
	model = glm::translate(model, glm::vec3(0.0f, 1.5f, -4.0));
	shader.setMat4("model", model);
	// render Cube
	glBindVertexArray(cubeVAO);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
	
	model = glm::mat4(1.0f);
	model = glm::translate(model, glm::vec3(4.0f, 0.0f, 2.0));
	shader.setMat4("model", model);
	// render Cube
	glBindVertexArray(cubeVAO);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
	
	model = glm::mat4(1.0f);
	model = glm::translate(model, glm::vec3(1.0f, 0.5f, 0.0f));
	model = glm::rotate(model, 60.0f, glm::normalize(glm::vec3(1.0, 0.0, 1.0)));
	model = glm::scale(model, glm::vec3(0.5));
	shader.setMat4("model", model);
	// render Cube
	glBindVertexArray(cubeVAO);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
}

